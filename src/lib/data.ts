// ─── Типы ────────────────────────────────────────────────────────────────────

export interface Question {
  text: string;
  subtext: string;
  options: { label: string; symbol: string; value: number }[];
}

export interface ResultType {
  title: string;
  subtitle: string;
  description: string;
  affirmations: string[];
  but: string;
  color: string;
  symbol: string;
}

export interface TestData {
  id: string;
  title: string;
  subtitle: string;
  symbol: string;
  description: string;
  questions: Question[];
  results: ResultType[];
}

export interface OracleCard {
  id: string;
  name: string;
  symbol: string;
  meaning: string;
  deepMessage: string;
  but: string;
  color: string;
}

export interface BreathingPreset {
  id: string;
  name: string;
  description: string;
  phases: { name: string; duration: number }[];
  symbol: string;
}

export interface LevelDef {
  name: string;
  xp: number;
  symbol: string;
}

export interface AchievementDef {
  id: string;
  name: string;
  description: string;
  symbol: string;
  xp: number;
  condition: (state: { streak: number; totalBreathing: number; testsCompleted: number; cardsDrawn: number; journalEntries: number }) => boolean;
}

